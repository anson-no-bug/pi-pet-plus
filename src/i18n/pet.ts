export function normalizeLocale(value: string | undefined | null): PetLocale {
	return value === "en" ? "en" : DEFAULT_PET_LOCALE;
}

export function t(
	locale: PetLocale,
	key: string,
	params: Record<string, string | number> = {},
): string {
	const localized = STRINGS[locale]?.[key] ?? STRINGS[DEFAULT_PET_LOCALE]?.[key] ?? STRINGS.en[key];
	if (!localized) return key;
	if (typeof localized === "function") return localized(params);
	return localized.replace(/\{(\w+)\}/g, (_match, name: string) => String(params[name] ?? `{${name}}`));
}

export function localeLabel(locale: PetLocale, target: PetLocale): string {
	return t(locale, `locale.${target}`);
}

export function newsPresentationLabel(locale: PetLocale, mode: NewsPresentationMode): string {
	return t(locale, `news.config.presentation.${mode}`);
}
